package homework6;

public class Homework6 {

    int sum() {
        System.out.println(0);
        return 0;
    }

    int sum(int a) {
        System.out.println(a);
        return a;
    }

    int sum(int a, int b) {
        int result = a + b;
        System.out.println(result);
        return result;
    }

    int sum(int a, int b, int c) {
        int result = a + b + c;
        System.out.println(result);
        return result;
    }

    int sum(int a, int b, int c, int d) {
        int result = a + b + c + d;
        System.out.println(result);
        return result;
    }
}
